void main() {
  String day = 'kj';
  switch (day) {
    case 'saturday':
      print('today is saturday');
      break;
    case 'monday':
      print('today is monday');
      break;
    case 'wensday':
      print('today is wensday');
      break;
    case 'thersday':
      print('today is thersday');
      break;
    default:
      print('invalid day');
      break;
  }
}
